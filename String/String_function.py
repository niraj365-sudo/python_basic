myStr = "abcdefghijklmnopqrstuvwxyz"

print(myStr.capitalize()) #Capitalize the first word(index 0)
print(len(myStr))   #print the length of string
print(myStr.startswith("abc"))  #Checks if the string starts with "abc" or not. If Yes, it returns true. If not, false.
print(myStr.endswith("xyz"))  #Checks if the string ends with "xyz" or not. If Yes, it returns true. If not, false.
print(myStr.count('a')) #Count how many 'a' is in string
print(myStr.upper())    #Convert all lower alphabet to upper.
print(myStr.lower())    #Convert all upper alphabet to lower.

